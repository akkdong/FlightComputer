#include "epd_drv.h"




////////////////////////////////////////////////////////////////////////////////////////
//

uint32_t pinLUT[256];




////////////////////////////////////////////////////////////////////////////////////////
//

void EPD_Init(void)
{
	// DATA Pin: GPIOD [12, 11, 7, 6, 5, 4, 3, 2]
	for (uint32_t i = 0; i < 256; i++)
	{
		uint32_t mask = ((i & 0b00111111) << 2) | (((i & 0b11000000) >> 6) << 11);
		pinLUT[i] = (((~mask) & 0b0001100011111100) << GPIO_NUMBER) | mask;
	}
}
