/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
#if ENABLE_NOR_TEST || 1
	MPU_Config();
#endif
  /* USER CODE END 1 */
/* USER CODE BEGIN Boot_Mode_Sequence_0 */
  int32_t timeout;
/* USER CODE END Boot_Mode_Sequence_0 */

  /* Enable I-Cache---------------------------------------------------------*/
  SCB_EnableICache();

  /* Enable D-Cache---------------------------------------------------------*/
  SCB_EnableDCache();

/* USER CODE BEGIN Boot_Mode_Sequence_1 */
  /* Wait until CPU2 boots and enters in stop mode or timeout*/
  timeout = 0xFFFF;
  while((__HAL_RCC_GET_FLAG(RCC_FLAG_D2CKRDY) != RESET) && (timeout-- > 0));
  if ( timeout < 0 )
  {
  Error_Handler();
  }
/* USER CODE END Boot_Mode_Sequence_1 */
  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

/* Configure the peripherals common clocks */
  PeriphCommonClock_Config();
/* USER CODE BEGIN Boot_Mode_Sequence_2 */
/* When system initialization is finished, Cortex-M7 will release Cortex-M4 by means of
HSEM notification */
/*HW semaphore Clock enable*/
__HAL_RCC_HSEM_CLK_ENABLE();
/*Take HSEM */
HAL_HSEM_FastTake(HSEM_ID_0);
/*Release HSEM in order to notify the CPU2(CM4)*/
HAL_HSEM_Release(HSEM_ID_0,0);
/* wait until CPU2 wakes up from stop mode */
timeout = 0xFFFF;
while((__HAL_RCC_GET_FLAG(RCC_FLAG_D2CKRDY) == RESET) && (timeout-- > 0));
if ( timeout < 0 )
{
Error_Handler();
}
/* USER CODE END Boot_Mode_Sequence_2 */

  /* USER CODE BEGIN SysInit */

	/* Check and handle if the system was resumed from StandBy mode */
	if(__HAL_PWR_GET_FLAG(PWR_FLAG_SB) != RESET)
	{
	  /* Clear system Standby flag */
	  __HAL_PWR_CLEAR_FLAG(PWR_FLAG_SB);
	}

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_FMC_Init();
  MX_QUADSPI_Init();
  MX_LPUART1_UART_Init();
  MX_I2C1_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  MX_RTC_Init();
  /* USER CODE BEGIN 2 */
  serial_init(&hlpuart1);


  /* The Following Wakeup sequence is highly recommended prior to each Standby mode entry
    mainly  when using more than one wakeup source this is to not miss any wakeup event.
    - Disable all used wakeup sources,
    - Clear all related wakeup flags,
    - Re-enable all used wakeup sources,
    - Enter the Standby mode.
  */
  /* Disable all used wakeup sources*/
  HAL_PWREx_DisableWakeUpPin(PWR_WAKEUP_PIN4);

  /* Re-enable all used wakeup sources*/
  /* Configure Wakeup pin */
  PWREx_WakeupPinTypeDef sPinParams;
  sPinParams.WakeUpPin    = PWR_WAKEUP_PIN4;
  sPinParams.PinPolarity  = PWR_PIN_POLARITY_LOW;
  sPinParams.PinPull      = PWR_PIN_NO_PULL;
  HAL_PWREx_EnableWakeUpPin(&sPinParams);

  /* Clear all related wakeup flags */
  HAL_StatusTypeDef ret = HAL_PWREx_ClearWakeupFlag(PWR_WAKEUP_FLAGS);
  if(ret != HAL_OK)
  {
    Error_Handler();
  }

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  //
  uint32_t count = 0;
  while (count++ < 10)
  {
	  HAL_GPIO_TogglePin(LED1_GPIO_Port, LED1_Pin);
	  HAL_Delay(100);
  }
  // turn-off
  HAL_GPIO_WritePin(LED1_GPIO_Port, LED1_Pin, GPIO_PIN_SET);
  printf("STM32H745 CoreBoard....\r\n");

  beep_init();
  shell_init();

#if DEPRECATED || 1
  uint32_t tone[] = { 240, 320, 470, 0, 600, 0 };
  uint32_t delay[] = { 200, 200, 200, 100, 400, 100 };
#if 0
  uint32_t tick = HAL_GetTick();
  int i = 0;
  beep_setFreq(tone[i]);
#else
  for (int i = 0; i < sizeof(tone) / sizeof(tone[0]); i++)
  {
	  beep_setFreq(tone[i]);
	  HAL_Delay(delay[i]);
  }
#endif
#endif

  uint32_t blinkTick = HAL_GetTick();
  int blinkType = 0;


  while (1)
  {
	  shell_process();

#if DEPRECATED || 0
	  if (HAL_GetTick() - tick > delay[i])
	  {
		  tick = HAL_GetTick();

		  i = (i + 1) % (sizeof(delay) / sizeof(delay[0]));
		  if (tone[i])
			  beep_setFreq(tone[i]);
		  else
			  beep_mute();
		  //printf("beep %d Hz   %ld\r\n", (int)tone[i], tick);
	  }
#endif
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  uint32_t blinkDelay = (blinkType == 0) ? 800 : 200;
	  if (HAL_GetTick() - blinkTick > blinkDelay)
	  {
		  blinkTick = HAL_GetTick();
		  HAL_GPIO_TogglePin(LED1_GPIO_Port, LED1_Pin);
		  blinkType = (1 - blinkType);
	  }
  }
  /* USER CODE END 3 */
}

